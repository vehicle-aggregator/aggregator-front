export function setError(control: AbstractControl | AbstractControl[] | null, valid: boolean, error: string = 'invalid') {
  if (!control) return;
  if (Array.isArray(control))
    control.forEach(c => setError(c, valid, error));
  else {
    // Keep other errors, remove or add only one
    let errors: ValidationErrors | null = control.errors || {};
    if (valid)
      delete errors[error];
    else
      errors[error] = true;
    if (Object.keys(errors).length === 0) errors = null;
    control.setErrors(errors);
  }
}

export function ASCIIValidator(c: AbstractControl) {
  let SPACE_REGEXP = /^ +| +$|( ) +/
  let ASCII_REGEXP = /^[ -~]+$/

  return c.value && (c.value?.match(SPACE_REGEXP) || !c.value?.match(ASCII_REGEXP)) ? { valid: false } : null;
}
